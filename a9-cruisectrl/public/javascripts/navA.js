function clickNav(index){
    switch(index){
        case 0:
            window.location.href = '/path/to/homeA';
            break;
        case 1:
            window.location.href = '/questionA';
            break;
        case 2:
            window.location.href = '/profileA';
            break;
        default:
            window.location.href = '/';
            break;
    }
}



