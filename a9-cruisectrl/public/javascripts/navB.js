function clickNav(index){
    switch(index){
        case 0:
            window.location.href = '/path/to/homeB';
            break;
        case 1:
            window.location.href = '/questionB';
            break;
        case 2:
            window.location.href = '/profileB';
            break;
        default:
            window.location.href = '/';
            break;
    }
}



